package oo;

public class TesteDoWhile {

	/*
	 * public static void main(String[] args) { int cont = 1; while (cont <= 10) {
	 * System.out.println(cont); cont += 1; }
	 */

	public static void main(String[] args) {
		int cont = 1;
		do {
			System.out.println(cont);
			cont++;
		} while (cont <= 10);
	}

}
